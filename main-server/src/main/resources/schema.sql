CREATE TABLE IF NOT EXISTS users (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  name VARCHAR(255) NOT NULL,
  email VARCHAR(512) NOT NULL,
  CONSTRAINT pk_user PRIMARY KEY (id),
  CONSTRAINT UQ_USER_EMAIL UNIQUE (email)
);

CREATE TABLE IF NOT EXISTS categories(
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  name VARCHAR(255) NOT NULL,
  CONSTRAINT UQ_CAT_NAME UNIQUE (name),
  CONSTRAINT pk_CAT PRIMARY KEY (id)
);

CREATE TABLE IF NOT EXISTS events(
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  annotation VARCHAR(2000) NOT NULL,
  category BIGINT REFERENCES categories ON DELETE RESTRICT,
  initiator BIGINT REFERENCES users ON DELETE RESTRICT,
  description VARCHAR(7000) NOT NULL,
  event_date TIMESTAMP WITHOUT TIME ZONE,
  created_on TIMESTAMP WITHOUT TIME ZONE,
  published_on TIMESTAMP WITHOUT TIME ZONE,
  loc_lat REAL NOT NULL,
  loc_lon REAL NOT NULL,
  paid BOOLEAN,
  participant_limit INTEGER,
  request_moderation BOOLEAN,
  title VARCHAR(120) NOT NULL,
  state VARCHAR(15) NOT NULL,
  CONSTRAINT pk_events PRIMARY KEY (id)
);
CREATE TABLE IF NOT EXISTS requests(
 id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
 created TIMESTAMP WITHOUT TIME ZONE,
 event BIGINT REFERENCES events ON DELETE CASCADE,
 requestor BIGINT REFERENCES users ON DELETE CASCADE,
 status VARCHAR(20),
 CONSTRAINT pk_requests PRIMARY KEY (id)
);
CREATE TABLE IF NOT EXISTS compilations(
 id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
 pinned BOOLEAN,
 title VARCHAR(255) NOT NULL,
 CONSTRAINT pk_compilations PRIMARY KEY (id)
);

CREATE TABLE IF NOT EXISTS compilation_events(
 compilation_id BIGINT REFERENCES compilations ON DELETE CASCADE,
 event_id BIGINT REFERENCES events ON DELETE CASCADE
);



